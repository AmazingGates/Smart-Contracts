# In oreder to make a transaction we first need to have a wallet.

# We are going to use a wallet called metamask.

# Metamask is a browser extension that allows you to interact with the ethereum blockchain.

# It is available on all major browsers, including chrome, firefox, and brave.

# You can also use it on mobile devices, including ios and android.

# To install metamask, go to the metamask website and click on the download button.

# This will take you to the chrome web store, where you can install the extension.

# Once you have installed metamask, you will see a fox icon in the top right corner of your browser.

# Click on the icon to open the extension.

# The first thing you will see is a welcome screen.

# Click on the get started button.

# This will take you to the next screen, where you can either create a new wallet or import an existing one.

# Since we are creating a new wallet, click on the create a wallet button.

# This will take you to the next screen, where you can create a password for your wallet.

# Enter a password and click on the create button.